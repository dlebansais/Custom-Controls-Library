namespace CustomControls
{
    /// <summary>
    /// Specifies operations on a tool.
    /// </summary>
    public enum ToolOperation
    {
        /// <summary>
        /// Show the tool.
        /// </summary>
        Show,

        /// <summary>
        /// Hide the tool.
        /// </summary>
        Hide,

        /// <summary>
        /// Toggle the tool's visibility.
        /// </summary>
        Toggle,
    }
}
