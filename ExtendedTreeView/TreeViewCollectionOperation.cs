namespace CustomControls;

/// <summary>
/// Specifies types of operation performed on a collection of tree view items.
/// </summary>
public enum TreeViewCollectionOperation
{
    /// <summary>
    /// Items are inserted.
    /// </summary>
    Insert,

    /// <summary>
    /// Items are removed.
    /// </summary>
    Remove,

    /// <summary>
    /// Items are moved.
    /// </summary>
    Move,
}
