namespace CustomControls
{
    /// <summary>
    /// Specifies operations on a solution.
    /// </summary>
    public enum SolutionOperation
    {
        /// <summary>
        /// Save the solution.
        /// </summary>
        Save,

        /// <summary>
        /// Create the solution.
        /// </summary>
        Create,

        /// <summary>
        /// Delete the solution.
        /// </summary>
        Delete,

        /// <summary>
        /// Open a solution.
        /// </summary>
        Open,

        /// <summary>
        /// Close the solution.
        /// </summary>
        Close,

        /// <summary>
        /// Exit the application.
        /// </summary>
        Exit,

        /// <summary>
        /// Export a document in the solution.
        /// </summary>
        ExportDocument,

        /// <summary>
        /// Export the solution.
        /// </summary>
        ExportSolution,

        /// <summary>
        /// Build the solution.
        /// </summary>
        Build,

        /// <summary>
        /// Paste something in the solution.
        /// </summary>
        Paste,
    }
}
